    public static String generateDate(int shift) {
        return LocalDate.now().plusDays(shift)                          //Текущая дата плюс shift дней
                .format(DateTimeFormatter.ofPattern("dd.MM.yyyy"));     //Формат даты день.месяц.год
    }

